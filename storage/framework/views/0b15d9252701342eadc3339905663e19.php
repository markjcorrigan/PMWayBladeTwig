      <h5 class="auto-style2">Now let us, by way of example, move from Left to Right across (and through) the PMBOK dashboard 
        (project processes) in a little more detail:</h5>
      <hr>
      <p align="left">Essentially, to run successful projects (after you have a Project Management Appointment Form signed and a sponsor 
        appointed 
        [and the sponsor should sign the appointment of you as a PM form!), 
        you must start with either<strong> process 4.1 </strong>(The Project 
        Charter) or<strong> process 13.1 </strong>(The Stakeholder Register) which activities fall firmly within the Initiation Process Group.&nbsp; 
        These two processes must be completed before moving into the Planning 
        Processes.&nbsp; Completion may only need to be at Version 1.&nbsp; 
        I.e. you can always come back and add to and improve your list of 
        Stakeholders and even update the Charter if required. Remember the 
        concept of iteration and incremenation.&nbsp; I.e. using version 
        control you can put 4 stakeholders into your stakeholder register in 
        version 1.&nbsp; Then after a few days you hear about another 
        stakeholder.&nbsp; You add them and update the contact details for one 
        of the fist 4 as version 2.&nbsp; I.e. in this way your stakeholder 
        register is growing and adjusting as you pin down you stakeholders. As 
        long as your sponsor is ok with and approves this.&nbsp; Also 
        approval, while a signature is often required, the simple fact of a 
        typed project log (short record) of what you have done, emailed to the 
        sponsor (stakeholders) and then saved will suffice as evidence that 
        you have 'improved the project documents / project knowledge' on your 
        project.&nbsp; I.e. making your running of the project more agile and 
        lean.&nbsp; <br>
      </p>
