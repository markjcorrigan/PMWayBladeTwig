      <hr>
      <h5 class="auto-style2">Know that the PMBOK is not a waterfall! </h5>
      <hr>
      <p align="left">You can move 
        through the processes both left to right and back to left again.&nbsp; 
        Also up and down / from beginning to end 
        of the dashboard.&nbsp; <br>
        I.e. You can turn 
        around and go backwards (against the process flow) when needed when, 
        for example, CHANGE CONTROL (4.6) 
        or other process changes are needed.&nbsp; <br>
        In addition (as you can see with process 4.7- Close Project or 
        PHASE) shorter project sprints are available 
        off the PMBOK dashboard.&nbsp; In this way you can iterate and 
        increment, step by step, towards a final solution.<br>
        This simple fact is often ignored by 
        people who criticize the PMI (and its Project Management Professional (PMP) Qualificaiton), saying it is a Traditional and Waterfall 
        methodology.</p>
      <p align="left">Always remember that while the PMBOK can be used as a method, 
        it is in fact a Body of Knowledge on the best way to run projects.<br>
        In this regard you can think firstly of the PMBOK as a list of 
        ingredients that can be used to run projects and then, using the 
        process groups, a method (recipe) that tells the cook where and when each 
        ingredient should be used.&nbsp; <br>
        In addition, know that PMBOK 
        version 6 is now fully Agile and Lean compatible with discussion in each process about how the process 
        can be used within Agile, Iterative and Adaptive environments!</p>
