          be in the position to kick start the project from a governance 
          perspective.&nbsp; I.e. think bus or train ticket collector.&nbsp; 
          If your charter (ticket) is not punched you cannot ride on this 
          project bus or train!&nbsp; So do not start the journey until your 
          ticket is approved and punched as valid by your sponsor (and their role and authority to punch the ticket is clearly set down in the Stakeholder Register (Process 13.1)!</p>
        <p align="left">To explain this very important PROJECT 
          INITIATION concept further:&nbsp; In the PRINCE2 Agile 
          (PRojects IN Controlled Environments) methodology, a 
          Project Board is responsible for the project and they appoint and 
          manage the Project Manager, who's job it is to serve them.&nbsp; 
          I.e. they must approve the project from a Business Case perspective 
          (that it will add value to the bottom line) before it is allowed to run.&nbsp; They must also approve the 
          project products that the project will be producing.&nbsp; At the end of the project the 
          Board must check and assess that value has in fact been delivered through the project 
          via its project products!</p>
