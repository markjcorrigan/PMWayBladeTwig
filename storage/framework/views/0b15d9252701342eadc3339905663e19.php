      <strong>
      <h5 >Tailoring and its importance!</h5>
      </strong>
      <hr>
      <p align="left">Remember that 'tailoring' (per the PMBOK guide) means that you must look 
        at each of the 49 processes and decide what you need to do from the 
        perspective of process ITTO's 
        (using your knowledge from the PMBOK guide) that you select (to use and to run) in your 
        particular project.&nbsp; Know that from a LEAN and AGILE perspective the 
        following rule must always apply:&nbsp; <strong>While BEST PRACTICE is understood, BEST PRACTICAL 
        is better! </strong>&nbsp;<br>
        Therefore the SKILL of the Project Management Professional 
        is in the ability to tailor their project towards LEAN and AGILITY!&nbsp; 
        That said, NEVER trade away your ability to land a project safely while 
        responding to pressure you will surely face to make your project go 
        faster!&nbsp; The fable of the tortoise and the hare from Aesop and going 
        off 'half-cocked' is 100% 
        applicable to this way of thinking.&nbsp; <a href="../fail.php" target="_blank" > <font color="black"><u>Some ideas from PMWay on this are here</u></font></a>.</p>
      <p align="left">As mentioned above a tailoring section exists in the dashboard above (if you click on it) for each process 
        where you can add your tailoring ideas (decisions).&nbsp; You then print these out to pdf and save these to your project repository as an audit trail to be used for 
        later reference or as evidence in an audit.<br>
        <br>
        Therefore, for the Initiating Process Group first Initiate the project 
        correctly by completing the Development of the Project Charter (4.1) and creating a list of Identified Stakeholders (13.1).</p>
      <hr>
      <h5 class="auto-style2">Then (and this is really important) PLAN the work before you 
        start doing (EXECUTING) the work!</h5>
<p>Agile perceptions about the value of long term planning <a href="/agile" target=_blank">are handled here.</a>  
      <hr>
      <p align="left">Here is something 
        you may not have thought of.&nbsp; Do you know about the Deming Cycle.&nbsp; <a href="../pdca.php" target="_blank"><font color="white"><u>This is PLAN, DO, CHECK and then ACT</u></font></a>.&nbsp; <br>
        Now, if you take a look at the 
        PMBOK dashboard, you will see that it uses exactly this approach!&nbsp; I.e. PLAN EXECUTE 
        MONITOR &amp; CONTROL and CHANGE CONTROL!<br>
        I.e. The quote:  "A goal without a plan is just a wish." attributed to Antoine de Saint-Exupery is worth bearing in mind here.<br>
      </p>
      <hr />
      <h5 class="auto-style2">NB: This plan does not 
        need to be WAR &amp; PEACE!&nbsp; In fact, if your Stakeholders 
        agree, it can be a single page 
        long!</h5>
      <hr />
      <p align="left"> I do suggest that your plan follow the Knowledge Areas (along the left side of the PMBOK Dashboard) so that you make sure you cover how you will handle the processes within each of these.&nbsp; 
      </p>
      <hr>
      <h5 class="auto-style2"> Now lets look at how this 
        (PLANNING PROCESS GROUP) works in more detail:</h5>
      <hr>
      <p align="left"> <strong>Can 
        you see that the Project Management Plan 4.2 is the sum total of all 
        of the processes (and Knowledge Area Plans) underneath it!<br>
        I.e. 4.2 =</strong> 5.1, 6.1, 7.1, 8.1, 9.1, 10.1, 11.1, 12.1 &amp; 13.2]).&nbsp; 
        Obviously the SCOPE PLAN (5.1) is the summary of the processes under 
        5.1 which are 5.2 =&nbsp; Collect 
        Requirements, 5.3 = Define Requirements and 5.4 =&nbsp; Create WBS 
        (Work Breakdown Structure).&nbsp; <br>
        If you think in terms of sub 
        plans to create the total plan then 5 = SCOPE, 6 = SCHEDULE, 7 = COST, 
        8 = QUALITY, 9 = RESOURCES, 10 = COMMUNICATIONS, 11= RISK, 12 =&nbsp; 
        PROCUREMENT AND 13 = STAKEHOLDERS will all build up to the consolidated Project 
        Management Plan (4.1).
      <p align="left">Only when the planning is done will you start to <strong> DO THE WORK </strong> as you DIRECT and MANAGE (4.3) project 
        work (EXECUTION) and MONITOR & CONTROL (4.4) the project to ensure that you remain 
        on track with your <strong>PLAN</strong>.&nbsp; Project Knowledge (a 
        resource to pin down knowledge learned while running your project / 
        which can be used to better run and assist with future projects etc.) is handled in 
        process 4.4.</p>
      <p align="left">This 'managing the flow' of processes systematically is 
        very important to ensure you are able to complete the project as you move to (<a href="../baseline" target="_blank"><font color="black"><u>'successfully jump the 
        planned project delivery gap'</u></font></a>).&nbsp; <br><br>

     <br>The key concept (tied to jumping the gap) is that of a project and system baseline. I.e. before you start a project 
        your system or project environment (configuration) will be at a 
        present baseline. The project plan aims, via an approved 
        Business Case, to move this baseline to a new and improved baseline.
        In a system build for example, if the new baseline proves to be a 
        problem then it should be possible to move back to a previous 
        baseline. This concept, and the Capability Maturity Level 2, 3 
        and 4 concept of bi-directional traceability is key.
