    <p align="left">Baseline is also used in terms of the project 
        schedule (Process 6.5 - Developed Schedule).&nbsp; I.e. this plan 
        (schedule) pulls together the who, what, when that is needed to get to 
        the new baseline.&nbsp; Typically project management software can lock 
        down the plan into a baseline so that variances against the plan can 
        be used when managing the project.  Earned Value Management is all 
        about bringing the project in on time, within budget and with an 
        agreed scope delivered. While agile will try to circumvent 
        the need to do EVM and monitor and control the plan (Iron Triangle), 
        PMWay suggests that the minute a project starts to run, costs are being 
        expended and delivery (budgets) must be planned and delivered, <strong>or else...<br>
        </strong>
      <hr>
      <p align="left">PMWay suggests that an inability of accounting (and the business 
        driven annual 
        accounting function / budgeting cycle) to adjust to a budget already approved 
        for a project is the root cause for blame for the negative perception of 
        project management and why projects are often run at Capability 
        Maturity level 1.&nbsp; I.e. low maturity appears to exist in this 
        area which insists on everyone JUST DO IT at all costs (CM L1) to 
        bring in a project, even though SCOPE etc., may have changed.<br><br>Why can a project manager not go back and 
        ask for a review of their project budget without fear of an hysterical 
        executive (CM L1)? I.e. At CM L2+ it will be understood that the need is to STOP the bus if 
        the SCOPE changes and adjust the project plan accordingly, which must 
        include an adjustment of the COST KNOWLEDGE AREA (and other associated 
        Knowledge Areas) of the PMBOK Dashboard; leading to a new plan and new schedule. I.e. this 
        schedule is not the old schedule and cannot be expected to exist on the 
        old (approved at that time) budget; else, if the new scope / 
        budget is not acceptable to the executive, then the only other option is 
        to terminate the project!<br>
        <br><br>
        Here is this thinking by means of a clear example:  You and your family decide to drive to a holiday destination.  Then, mid holiday, which is not as great as you expected, you decide to change your plan and spend time with relatives 4 hours drive further along the coast than your holiday destination.  I.e. your plan changes and as driver 
        (head of the family etc.), you, after buy in from your family (spouse, 
        kids etc.), simply spend the extra money for the change in experience, if you can afford it.
      <hr>
      <p align="left">If change in the project plan is required then the Change Control Board (CCB) and your Change Control Process (4.6) 
        is used to ensure the sub plans (which add up to the 
        total Project Management Plan 4.2) are updated to include new deliverables (work packages) in the revised Work 
        Breakdown Structure and Schedule etc., are all in sync with the new (agreed / 
        approved) requirements. <br>
        <br>
        Following the processes the Project Manager will be able to reach the 
        end of the project (per <strong>latest agreed Project Management Plan</strong> and the project can be <strong>CLOSED</strong> (4.7). </p>
